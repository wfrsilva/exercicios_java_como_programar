package Capitulo_5_Instrucoes_de_controle_parte_2.Figura_5_2_ForCounter_java;

public class ForCounter{

    public static void main(String [] args ){
        
        for (int counter = 1; counter <=10; counter++){
            System.out.printf("%d ", counter);
        }//for

        System.out.println();

    }//main

}//ForCounter